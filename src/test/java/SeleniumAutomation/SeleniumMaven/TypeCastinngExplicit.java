package SeleniumAutomation.SeleniumMaven;

public class TypeCastinngExplicit {

	public static void main(String[] args) {

		float f = 10.5f;
		double d = 100.5d;
		
		int n = (int)f;
		int m = (int)d;
		
		System.out.println("float is "+f +" Corresponding integer is "+n);
		System.out.println("double is "+d +" Corresponding integer is "+m);
	
	}

}
